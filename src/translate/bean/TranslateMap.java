package translate.bean;

public class TranslateMap {
	/**
	 * 翻译前的内容
	 */
	private String src = "";
	/**
	 * 翻译后的内容
	 */
	private String dst = "";

	public String getSrc() {
		return src;
	}

	public void setSrc(String src) {
		this.src = src;
	}

	public String getDst() {
		return dst;
	}

	public void setDst(String dst) {
		this.dst = dst;
	}

}
